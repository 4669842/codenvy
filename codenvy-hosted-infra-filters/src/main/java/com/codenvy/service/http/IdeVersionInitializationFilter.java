package com.codenvy.service.http;

import javax.inject.Singleton;
import javax.servlet.Filter;
import javax.servlet.FilterChain;
import javax.servlet.FilterConfig;
import javax.servlet.ServletException;
import javax.servlet.ServletRequest;
import javax.servlet.ServletResponse;
import java.io.IOException;

/**
 * Sets current IDE version to the thread local variable.
 */
@Singleton
public class IdeVersionInitializationFilter implements Filter {
    @Override
    public void init(FilterConfig filterConfig) throws ServletException {

    }

    @Override
    public void doFilter(ServletRequest servletRequest, ServletResponse servletResponse, FilterChain filterChain)
            throws IOException, ServletException {

        try {
            String host = servletRequest.getServerName();
            if (host.toLowerCase().startsWith("c2.")) {
                IdeVersionHolder.set(true);
            }
        } finally {
            filterChain.doFilter(servletRequest, servletResponse);
            IdeVersionHolder.remove();
        }
    }

    @Override
    public void destroy() {

    }
}
